public class Z24 {
    public static void main(String[] args) {
        int [] elements = {10,20,30,500,700};
        for (int i: elements) {
            System.out.println(i);//
        }
        System.out.println("---------------");
        for (int i = 0; i < elements.length; i++) {
            System.out.println(elements[i]);
        }
        System.out.println("---------------");
    }
}
